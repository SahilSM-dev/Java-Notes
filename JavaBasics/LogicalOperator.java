package JavaBasics;

public class LogicalOperator {
    public static void main(String[] args){
        System.out.println("Logical Operator");
        int a = 4;
        int b = 7;
        System.out.println("Logical AND");
        System.out.println(a < 3 && a != b);
        System.out.println(a > 3 && a != b);
        System.out.println("Logical OR");
        System.out.println(a < 3 || a != b);
        System.out.println(a > 3 || a != b);
    }
}
